void	ft_swap(int *a, int *b)
{
	int	n;

	n = *b;
	*b = *a;
	*a = n;
}

#include <stdio.h>
int main()
{
	int a=5;
	int b=6;
	ft_swap(&a, &b);
	printf("%d, %d", a, b);
}